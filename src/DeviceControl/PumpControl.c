/* 
 * ------------------------------------------------------ * 
 * @file	: PumpControl.c
 * @brief	: 給湯ポンプの制御を行う
 * ------------------------------------------------------ * 
 */
#include "PumpControl.h"


/* 
 * ------------------------------------------------------ * 
 * @function: ポンプの初期化を行う
 * @param	: void
 * @return	: void
 * ------------------------------------------------------ * 
 */
void initPump(void){
	DA.DR1 = 0x00;
	DA.CR.BIT.DAOE1 = 1;
}


/* 
 * ------------------------------------------------------ * 
 * @function: 給湯を行う
 * @param	: void
 * @return	: void
 * ------------------------------------------------------ * 
 */
void doPump(void){
	DA.DR1 = 255;
}


/* 
 * ------------------------------------------------------ * 
 * @function: 給湯を止める
 * @param	: void
 * @return	: void
 * ------------------------------------------------------ * 
 */
void stopPump(void){
	DA.DR1 = 0;
}
